/**
 * Get translated string by key.
 * Supports string replacement with {0}, {1}, etc.
 */
export const translate = (lang, key, ...args) => {
  const currentLangDict = translations[lang] || translations['en-US'];
  
  // Handle nested keys like 'categories.produce'
  const keys = key.split('.');
  let text = currentLangDict;
  
  for (const k of keys) {
    if (text && text[k]) {
      text = text[k];
    } else {
      // Fallback to English if key missing in target language
      text = translations['en-US'];
      for (const enK of keys) {
        if (text && text[enK]) text = text[enK];
        else {
           text = key;
           break;
        }
      }
      break;
    }
  }

  if (typeof text !== 'string') return key;

  // Replace {0}, {1} with arguments
  args.forEach((arg, i) => {
    text = text.replace(new RegExp(`\\{${i}\\}`, 'g'), arg);
  });

  return text;
};
